package main

import "fmt"

func main() {
	// Operasi Perbadingan(Comparison)

	// > greater than
	// < less than
	// >= greater than or equal to
	// <= less than or equal to
	// == equal to
	// != not equal to

	var name1 string = "John"
	var name2 string = "John"

	result := name1 < name2
	fmt.Println(result)
}
